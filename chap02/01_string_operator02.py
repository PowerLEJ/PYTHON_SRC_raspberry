# 슬라이싱[시작값:끝값+1]
print("안녕하세요"[1:4]) # 녕하세 # 마지막 숫자를 포함하지 않음
print("안녕하세요"[2:0]) # 아무것도 출력되지 않음
print("안녕하세요"[2:-1]) # 하세
print("안녕하세요"[2:]) # 하세요
print("안녕하세요"[:3]) # 안녕하
print("안녕하세요"[-3:]) # 하세요
print("안녕하세요"[-4:-2]) # 녕하

